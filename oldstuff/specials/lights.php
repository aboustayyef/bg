		<h3>Metlaspot Lights</h3>
		<p>Milan, 1957. At a few steps from the Duomo, Orazio Spada gave birth to his first handmade creations, forging with great skill raw metals. The urge for a continuous research brought to a final focus on design and to a balance between technique and product quality.</p>
		<p>With the establishment of Metal Spot, in 1977, the craftsmanâ€™s approach has met the newest of technologies, although without abandoning its focus in manufacturing objects marked both by aesthetic and qualitative values. The soul of the company merges into a production that can be labelled as technical-decorative, with a contemporary style and a high design and technology content.</p>
		<p>Still managed by the Spada family, Metal Spot has been designing with an innovative outlook, using new types of light sources and materials, with special care in improving the performance in terms of lighting technologies and reducing energy consumptions.</p>
		<p>2012 opens a further evolution: the idea of casting ourselves towards a long lasting design, going through fashions, tastes and generations transversally.</p>
